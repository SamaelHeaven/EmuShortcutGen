namespace GScraper.Google;

/// <summary>
/// Contains the possible licenses (usage rights) in Google Images.
/// </summary>
public static class GoogleImageLicenses
{
    /// <summary>
    /// Returns the code for Creative Commons licenses.
    /// </summary>
    public const string CreativeCommons = "cl";

    /// <summary>
    /// Returns the code for Commercial and other licenses.
    /// </summary>
    public const string Commercial = "ol";
}
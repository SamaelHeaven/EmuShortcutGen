    /// </summary>
    Share,
    /// <summary>
    /// Free to Share and Use Commercially.
    /// </summary>
    ShareCommercially,
    /// <summary>
    /// Free to Modify, Share and Use.
    /// </summary>
    Modify,
    /// <summary>
    /// Free to Modify, Share and Use Commercially.
    /// </summary>
    ModifyCommercially
}
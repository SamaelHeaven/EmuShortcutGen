namespace GScraper;

/// <summary>
/// Specifies the Safe Search levels.
/// </summary>
public enum SafeSearchLevel
{
    /// <summary>
    /// Safe Search is off.
    /// </summary>
    Off,

    /// <summary>
    /// Don't show explicit results.
    /// </summary>
    Moderate,

    /// <summary>
    /// Strict safe search.
    /// </summary>
    Strict
}